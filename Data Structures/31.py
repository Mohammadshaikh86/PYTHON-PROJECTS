# Create a list of integers
numbers = [1, 3, 5, 7, 9]
print("Original list:", numbers)

# Append
numbers.append(18)
print("After append:", numbers)

# Insert
numbers.insert(2, 3)
print("After insert:", numbers)

# Remove
numbers.remove(5)
print("After remove:", numbers)

# Pop
popped = numbers.pop()
print("After pop:", numbers)
print("Popped element:", popped)
